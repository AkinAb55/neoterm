#ifndef _UK_NET_IEEE80211_RADIOTAP_H
#define _UK_NET_IEEE80211_RADIOTAP_H
#include <linux/types.h>

#define PKTHDR_RADIOTAP_VERSION 0
/* radiotap CHANNEL mező flag-jei */
#define IEEE80211_CHAN_TURBO  0x0010
#define IEEE80211_CHAN_CCK    0x0020
#define IEEE80211_CHAN_OFDM   0x0040
#define IEEE80211_CHAN_2GHZ   0x0080
#define IEEE80211_CHAN_5GHZ   0x0100
#define IEEE80211_CHAN_PASSIVE 0x0200
#define IEEE80211_CHAN_DYN    0x0400
#define IEEE80211_CHAN_GFSK   0x0800

struct ieee80211_radiotap_header { u8 it_version; u8 it_pad; __le16 it_len; __le32 it_present; } __attribute__((packed));

/* radiotap mezőtípusok (a 'present' bitmaszk indexei) */
enum ieee80211_radiotap_type {
	IEEE80211_RADIOTAP_TSFT = 0,
	IEEE80211_RADIOTAP_FLAGS = 1,
	IEEE80211_RADIOTAP_RATE = 2,
	IEEE80211_RADIOTAP_CHANNEL = 3,
	IEEE80211_RADIOTAP_FHSS = 4,
	IEEE80211_RADIOTAP_DBM_ANTSIGNAL = 5,
	IEEE80211_RADIOTAP_DBM_ANTNOISE = 6,
	IEEE80211_RADIOTAP_LOCK_QUALITY = 7,
	IEEE80211_RADIOTAP_TX_ATTENUATION = 8,
	IEEE80211_RADIOTAP_DB_TX_ATTENUATION = 9,
	IEEE80211_RADIOTAP_DBM_TX_POWER = 10,
	IEEE80211_RADIOTAP_ANTENNA = 11,
	IEEE80211_RADIOTAP_DB_ANTSIGNAL = 12,
	IEEE80211_RADIOTAP_DB_ANTNOISE = 13,
	IEEE80211_RADIOTAP_RX_FLAGS = 14,
	IEEE80211_RADIOTAP_TX_FLAGS = 15,
	IEEE80211_RADIOTAP_RTS_RETRIES = 16,
	IEEE80211_RADIOTAP_DATA_RETRIES = 17,
	IEEE80211_RADIOTAP_MCS = 19,
	IEEE80211_RADIOTAP_AMPDU_STATUS = 20,
	IEEE80211_RADIOTAP_VHT = 21,
	IEEE80211_RADIOTAP_RADIOTAP_NAMESPACE = 29,
	IEEE80211_RADIOTAP_VENDOR_NAMESPACE = 30,
	IEEE80211_RADIOTAP_EXT = 31,
};
/* FLAGS mező bitjei */
#define IEEE80211_RADIOTAP_F_CFP      0x01
#define IEEE80211_RADIOTAP_F_SHORTPRE 0x02
#define IEEE80211_RADIOTAP_F_WEP      0x04
#define IEEE80211_RADIOTAP_F_FRAG     0x08
#define IEEE80211_RADIOTAP_F_FCS      0x10
#define IEEE80211_RADIOTAP_F_DATAPAD  0x20
#define IEEE80211_RADIOTAP_F_BADFCS   0x40

/* MCS mező */
#define IEEE80211_RADIOTAP_MCS_HAVE_BW    0x01
#define IEEE80211_RADIOTAP_MCS_HAVE_MCS   0x02
#define IEEE80211_RADIOTAP_MCS_HAVE_GI    0x04
#define IEEE80211_RADIOTAP_MCS_HAVE_STBC  0x20
#define IEEE80211_RADIOTAP_MCS_SGI        0x04
#define IEEE80211_RADIOTAP_MCS_STBC_MASK  0x60
#define IEEE80211_RADIOTAP_MCS_STBC_SHIFT 5
#define IEEE80211_RADIOTAP_MCS_HAVE_FEC   0x10
#define IEEE80211_RADIOTAP_MCS_FEC_LDPC   0x10
#define IEEE80211_RADIOTAP_MCS_BW_MASK    0x03
#define IEEE80211_RADIOTAP_MCS_BW_20      0
#define IEEE80211_RADIOTAP_MCS_BW_40      1
#define IEEE80211_RADIOTAP_MCS_BW_20L     2
#define IEEE80211_RADIOTAP_MCS_BW_20U     3
#define IEEE80211_RADIOTAP_TX_FLAGS_VAL   0
#define IEEE80211_RADIOTAP_F_TX_NOACK     0x0008
#define IEEE80211_RADIOTAP_CODING_LDPC_USER0 0x01
#define IEEE80211_RADIOTAP_VHT_FLAG_LDPC  0x10
/* VHT mező */
#define IEEE80211_RADIOTAP_VHT_KNOWN_STBC      0x0001
#define IEEE80211_RADIOTAP_VHT_KNOWN_GI        0x0004
#define IEEE80211_RADIOTAP_VHT_KNOWN_BANDWIDTH 0x0040
#define IEEE80211_RADIOTAP_VHT_FLAG_STBC       0x01
#define IEEE80211_RADIOTAP_VHT_FLAG_SGI        0x04

struct ieee80211_radiotap_iterator {
	const struct ieee80211_radiotap_header *_rtheader;
	int _max_length; int _arg_index; u8 *this_arg; int this_arg_index; int this_arg_size;
	const u8 *_arg; const u8 *_next_ns_data;
};
int ieee80211_radiotap_iterator_init(struct ieee80211_radiotap_iterator *it, struct ieee80211_radiotap_header *h, int len, const void *ns);
int ieee80211_radiotap_iterator_next(struct ieee80211_radiotap_iterator *it);
static inline int ieee80211_get_radiotap_len(const unsigned char *data){ const struct ieee80211_radiotap_header *h=(const void*)data; return h->it_len; }
#endif
